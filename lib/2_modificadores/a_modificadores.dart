void main() {
  var nome;
  nome = "Carlos";
  print(nome);

  //final
  final nomeCompletoFinal = "Carlos";
  final nomeCompletoFinal2 = nome;
  //se torna imutavel, não podem ser alteradas depois de inicializadas
  //são definidas em tempo de execução runtime

  //const
  final nomeCompletoConst = "Carlos";
  //são definidas em tempo de compilação, não podem receber valores que não sejam constantes
}
